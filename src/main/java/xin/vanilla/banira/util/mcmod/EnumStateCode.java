    private static final Map<Integer, EnumStateCode> CODE_MAP = new HashMap<>();

    static {
        for (EnumStateCode value : values()) {
            CODE_MAP.put(value.code, value);
        }
    }

    private final int code;
    private final String message;

    EnumStateCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public static @Nullable EnumStateCode valueOfCode(int code) {
        return CODE_MAP.get(code);
    }

    public static @Nullable EnumStateCode valueOfCode(@Nullable String code) {
        if (StringUtils.isNullOrEmptyEx(code)) {
            return null;
        }
        try {
            return valueOfCode(Integer.parseInt(code.trim()));
        } catch (NumberFormatException ignored) {
            return null;
        }
    }

    public static @Nonnull String messageOf(@Nullable Integer code) {
        if (code == null) {
            return C999.message();
        }
        EnumStateCode stateCode = valueOfCode(code);
        return stateCode != null ? stateCode.message() : C999.message() + "（" + code + "）";
    }

    public static @Nonnull String messageOf(@Nullable String code) {
        EnumStateCode stateCode = valueOfCode(code);
        return stateCode != null ? stateCode.message() : C999.message();
    }

    public @Nonnull String formatMessage(Object... args) {
        if (args == null || args.length == 0) {
            return message;
        }
        return String.format(message, args);
    }
